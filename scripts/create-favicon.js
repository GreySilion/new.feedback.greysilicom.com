const path = require('path');

// This is a minimal ICO file header for a 32x32 image
const icoHeader = Buffer.from([
  0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x20, 0x20, 0x00, 0x00, 0x01, 0x00, 0x20, 0x00, 0x28, 0x10,
  0x00, 0x00, 0x16, 0x00, 0x00, 0x00, 0x28, 0x00, 0x00, 0x00, 0x20, 0x00, 0x00, 0x00, 0x40, 0x00,
  0x00, 0x00, 0x01, 0x00, 0x20, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x00,
  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00
]);

// Create a simple 32x32 black square as a placeholder
const imageData = Buffer.alloc(1024, 0); // 32x32 pixels with 1 byte per channel (RGBA)

// Combine header and image data
const icoFile = Buffer.concat([icoHeader, imageData]);

// Write to favicon.ico
fs.writeFileSync('public/favicon.ico', icoFile);

